import autoload from '@fastify/autoload';
import swagger from '@fastify/swagger';
import ScalarApiReference from '@scalar/fastify-api-reference';
import Fastify from 'fastify';
import path from 'path';
import config from './config.js';

const BASE_URL = '/' + (config.server.baseUrl?.trim().replace(/^\/*|\/*$/g, '').trim() ?? '');
const API_DOCS_URL = 'api-docs';

export default async function app() {

    const server = Fastify({
        logger: true,
        ignoreTrailingSlash: true
    });

    let version = 'v1';
    const VERSION_BASE_URL = `${BASE_URL}/${version}`.trim().replace(/\/+/g, '/').trim();

    // configure swagger
    const swaggerConfig = {
        openapi: {
            openapi: '3.0.3',
            info: {
                title: 'Geodata Conversion API',
                description: 'This API converts geometry information from and to various formats. Additionally, it can create centroid and bbox information.',
                version: config.server.build.version
            },
            servers: [{ url: VERSION_BASE_URL }]
        }
    }
    await server.register(swagger, swaggerConfig)

    // register routes
    const routesBaseDir = path.resolve(import.meta.dirname, `api/${version}/routes`);
    await server.register(autoload, {
        dir: routesBaseDir,
        forceESM: true,
        indexPattern: /^index\.(js|ts)$/,
        maxDepth: 1,
        routeParams: true,
        options: { prefix: VERSION_BASE_URL },
        dirNameRoutePrefix: (folderParent: string, folderName: string) => {
            return `/${folderName}`;
        }
    });

    // route for api documentation generated by scalar
    const apiDocsUrl = `${VERSION_BASE_URL}/${API_DOCS_URL}`;
    await server.register(ScalarApiReference, {
        routePrefix: apiDocsUrl,
        configuration: {
            spec: () => server.swagger(),
        },
    });

    return server;
}
